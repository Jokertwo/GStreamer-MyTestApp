package com.aveco.Gstreamer.testRunnable;

import java.awt.Color;


public class Pair {

    private String value;
    private Color color;


    public Pair(String value, Color color) {
        super();
        this.value = value;
        this.color = color;
    }


    public String getValue() {
        return value;
    }


    public Color getColor() {
        return color;
    }

}
